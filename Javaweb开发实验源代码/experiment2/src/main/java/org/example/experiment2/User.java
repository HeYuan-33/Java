package org.example.experiment2;

public class User {

        private String uname;
        private String upwd;
        private int uage;

        // Getters and Setters
        public String getUname() {
            return uname;
        }

        public void setUname(String uname) {
            this.uname = uname;
        }

        public String getUpwd() {
            return upwd;
        }

        public void setUpwd(String upwd) {
            this.upwd = upwd;
        }

        public int getUage() {
            return uage;
        }

        public void setUage(int uage) {
            this.uage = uage;
        }
    }

